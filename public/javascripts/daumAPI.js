function setCenter(x1, y0) {            
    let level =  2;
    // 이동할 위도 경도 위치를 생성합니다 
    var moveLatLon = new kakao.maps.LatLng(x1, y0);
    
    // 지도 중심을 이동 시킵니다
    map.setCenter(moveLatLon);
    map.setLevel(level);
}

function addMarker(markerArr) {
    if(marker.length > 0) {
        console.log("maker reset");
        markerReset();
    }
    markerArr.forEach((e) => {
        marker.push( new kakao.maps.Marker({
            position: new kakao.maps.LatLng(e[1], e[0])
        }));
    });

    //마커 지도 위에 설정
    marker.forEach(e => {e.setMap(map);})
}
function markerReset() {
    marker.forEach((e) => {
        e.setMap(null);
    });
    marker = new Array();
}

function addPoly(path) {
    var linePath = new Array;
    path.forEach(e => {linePath.push(new kakao.maps.LatLng(e[1], e[0]));});
    console.log(linePath);

    if(polyline) {
        polyline.setMap(null);  
    }

    // 지도에 표시할 선을 생성합니다
    polyline = new kakao.maps.Polyline({
        path: linePath, // 선을 구성하는 좌표배열 입니다
        strokeWeight: 4, // 선의 두께 입니다
        strokeColor: '#283593', // 선의 색깔입니다
        strokeOpacity: 1, // 선의 불투명도 입니다 1에서 0 사이의 값이며 0에 가까울수록 투명합니다
        strokeStyle: 'solid' // 선의 스타일입니다
    });
    
    // 지도에 선을 표시합니다 
    polyline.setMap(map);  
}
